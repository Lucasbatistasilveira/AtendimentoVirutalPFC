public class ResponseLogin {
	String serverMensagem;
	String status;

	public String getServerMensagem() {
		return serverMensagem;
	}
	public void setServerMensagem(String serverMensagem) {
		this.serverMensagem = serverMensagem;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
}
